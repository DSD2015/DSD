package bean;

import java.util.*;

public class Solicitud {
	
	
	private Integer codsolicitud;
	private Integer codcli;
	private String  desperfil;
	private String  desproyecto;
	private String  estsolicitud;
	private Date    fecCreacion;
	private Date    feFin;
	private Date    feInicio;
	
	
	
	
	public Integer getCodsolicitud() {
		return codsolicitud;
	}
	public void setCodsolicitud(Integer codsolicitud) {
		this.codsolicitud = codsolicitud;
	}
	public Integer getCodcli() {
		return codcli;
	}
	public void setCodcli(Integer codcli) {
		this.codcli = codcli;
	}
	public String getDesperfil() {
		return desperfil;
	}
	public void setDesperfil(String desperfil) {
		this.desperfil = desperfil;
	}
	public String getDesproyecto() {
		return desproyecto;
	}
	public void setDesproyecto(String desproyecto) {
		this.desproyecto = desproyecto;
	}
	public String getEstsolicitud() {
		return estsolicitud;
	}
	public void setEstsolicitud(String estsolicitud) {
		this.estsolicitud = estsolicitud;
	}
	public Date getFecCreacion() {
		return fecCreacion;
	}
	public void setFecCreacion(Date fecCreacion) {
		this.fecCreacion = fecCreacion;
	}
	public Date getFeFin() {
		return feFin;
	}
	public void setFeFin(Date feFin) {
		this.feFin = feFin;
	}
	public Date getFeInicio() {
		return feInicio;
	}
	public void setFeInicio(Date feInicio) {
		this.feInicio = feInicio;
	}
	
	
	
	
	

}
